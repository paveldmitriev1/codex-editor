def get_token():
    env = Path.home() / ".cc-memory-bridge/.env"
    if not env.exists():
        return None
    for line in env.read_text().splitlines():
        if line.startswith("CLAUDE_CODE_OAUTH_TOKEN="):
            return line.split("=", 1)[1].strip()
    return None


def load_critics_config() -> dict:
    if CRITICS_CONFIG.exists():
        try:
            return json.loads(CRITICS_CONFIG.read_text(encoding="utf-8"))
        except Exception:
            pass
    CRITICS_CONFIG.parent.mkdir(parents=True, exist_ok=True)
    CRITICS_CONFIG.write_text(
        json.dumps(DEFAULT_CRITICS, indent=2, ensure_ascii=False), encoding="utf-8")
    return DEFAULT_CRITICS


def save_critics_config(cfg: dict):
    CRITICS_CONFIG.parent.mkdir(parents=True, exist_ok=True)
    CRITICS_CONFIG.write_text(
        json.dumps(cfg, indent=2, ensure_ascii=False), encoding="utf-8")


def load_global_context() -> str:
    """Контекст всего Кодекса (для критиков с context_mode='global_context')."""
    parts = []
    if CANON_PATH.exists():
        parts.append("# CANON\n\n" + CANON_PATH.read_text(encoding="utf-8")[:3000])
    if CHAPTERS_DIR.exists():
        for book_dir in sorted(CHAPTERS_DIR.iterdir()):
            if not book_dir.is_dir() or book_dir.name.startswith("."):
                continue
            for ch_dir in sorted(book_dir.iterdir()):
                if not ch_dir.is_dir() or ch_dir.name.startswith("."):
                    continue
                draft = ch_dir / "draft.md"
                if not draft.exists():
                    continue
                try:
                    text = draft.read_text(encoding="utf-8")
                    title = text.split("\n")[0][:80] if text else ch_dir.name
                    parts.append(f"\n## {ch_dir.name}\n{title}\n{text[:500]}...\n")
                except Exception:
                    continue
    return "\n".join(parts)[:50000]


def run_critic(critic_id: str, chapter_text: str, global_context: str = "",
               other_critics_results: dict = None) -> dict:
    cfg = load_critics_config()
    c = cfg.get(critic_id)
    if not c or not c.get("enabled"):
        return {"ok": False, "error": f"critic {critic_id} disabled or missing"}
    token = get_token()
    if not token:
        return {"ok": False, "error": "no OAuth token"}

    system = c["system"]
    if c.get("context_mode") == "global_context" and global_context:
        system += f"\n\n# КОНТЕКСТ ВСЕГО КОДЕКСА:\n{global_context}"

    user_content = chapter_text[:30000]
    if c.get("context_mode") == "chapter_plus_other_critics" and other_critics_results:
        user_content += f"\n\n# РЕЗУЛЬТАТЫ ОСТАЛЬНЫХ КРИТИКОВ (для synthesis):\n{json.dumps(other_critics_results, ensure_ascii=False)[:20000]}"

    body = {
        "model": c.get("model", MAX_MODEL),
        "max_tokens": 8000,
        "thinking": {"type": "enabled", "budget_tokens": c.get("thinking_budget", 4000)},
        "system": system,
        "messages": [{"role": "user", "content": f"Прочитай главу и отдай строго JSON (без preamble):\n\n{user_content}"}],
    }
    req = urllib.request.Request(
        f"{PROXY_URL}/v1/messages",
        data=json.dumps(body).encode("utf-8"),
        headers={
            "x-api-key": token,
            "anthropic-version": "2023-06-01",
            "anthropic-beta": "interleaved-thinking-2025-05-14",
            "content-type": "application/json",
        },
    )
    try:
        with urllib.request.urlopen(req, timeout=900) as resp:
            data = json.loads(resp.read().decode("utf-8"))
    except urllib.error.HTTPError as e:
        return {"ok": False, "error": f"HTTP {e.code}: {e.read().decode('utf-8', errors='replace')[:300]}"}
    except Exception as e:
        return {"ok": False, "error": str(e)}

    text_blocks = [b.get("text", "") for b in data.get("content", []) if b.get("type") == "text"]
    raw_text = "\n".join(text_blocks).strip()
    result = None
    try:
        clean = raw_text.strip()
        if clean.startswith("```"):
            clean = clean.split("\n", 1)[1] if "\n" in clean else clean
            if clean.endswith("```"):
                clean = clean.rsplit("```", 1)[0]
            if clean.startswith("json"):
                clean = clean.split("\n", 1)[1] if "\n" in clean else clean
        result = json.loads(clean.strip())
    except Exception:
        result = {"raw_text": raw_text[:2000]}
    return {
        "ok": True,
        "critic": critic_id,
        "label": c.get("label", critic_id),
        "group": c.get("group", "technical"),
        "result": result,
        "usage": data.get("usage", {}),
    }


def run_all_enabled(chapter_text: str, global_context: str = "") -> dict:
    cfg = load_critics_config()
    results = {}
    # 1. Запускаем все КРОМЕ synthesis
    for cid, c in cfg.items():
        if not c.get("enabled") or cid == "synthesis":
            continue
        print(f"  → {cid}: {c.get('label')}")
        results[cid] = run_critic(cid, chapter_text, global_context)
    # 2. synthesis читает результаты остальных
    if cfg.get("synthesis", {}).get("enabled"):
        print(f"  → synthesis (читает {len(results)} критиков)")
        results["synthesis"] = run_critic("synthesis", chapter_text,
                                           global_context, other_critics_results=results)
    return {
        "ts": datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ"),
        "model": MAX_MODEL,
        "critics_run": list(results.keys()),
        "results": results,
    }


